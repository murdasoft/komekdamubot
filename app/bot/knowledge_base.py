def detect_intent(text: str) -> Optional[str]:
    """Detect product intent from text (DAMU и ипотека — выше бизнес-кредита)."""
    text_lower = text.lower()

    priority = (
        "damu",
        "mortgage_gov",
        "mortgage_standard",
        "refinancing",
        "personal_credit",
        "business_credit",
    )
    for intent in priority:
        keywords = INTENT_KEYWORDS.get(intent, {})
        for words in keywords.values():
            for word in words:
                if word in text_lower:
                    return intent
    return None


def is_damu_ip_question(text: str) -> bool:
    """Вопрос про DAMU для ИП / без залога."""
    low = text.lower()
    if not any(w in low for w in ("даму", "damu", "12,6", "12.6")):
        return False
    if any(w in low for w in ("беззалог", "кепілдіксіз", "залогсыз", "залогом")):
        return True
    return any(w in low for w in ("ип", "жк", "жс", "индивидуальн"))


