def pick_voice_text_nudge(lang: str, chat_id: str) -> str:
    pool = VOICE_TEXT_NUDGES_KK if lang == "kk" else VOICE_TEXT_NUDGES_RU
    idx = int(hashlib.md5(f"{chat_id}:voice".encode()).hexdigest(), 16) % len(pool)
    return pool[idx]


def get_lang_retry_message(lang: str, attempt: int) -> str:
    pool = LANG_RETRY_KK if lang == "kk" else LANG_RETRY_RU
    return pool[min(attempt, len(pool) - 1)]


def parse_lang_digit(text: str) -> str | None:
    """«Қазақша» / «русский» / 1 / 2 → цифра шага языка."""
    t = text.strip()
    if t in ("1", "2"):
        return t
    low = t.lower().replace("ё", "е")
    kk = (
        "қазақша", "казакша", "қазақ", "казах", "казак", "qazaq", "qazaqsha",
        "kazakh", "kz", "казахский", "на казахском",
    )
    ru = (
        "русский", "русском", "русски", "орысша", "орыс", "russian", "ru",
        "на русском", "по-русски",
    )
    for w in kk:
        if w in low:
            return "1"
    for w in ru:
        if w in low:
            return "2"
    if any(c in low for c in "әіңғүұқөһ"):
        return "1"
    return None


def is_vague_credit_request(text: str) -> bool:
    """«Хочу кредит» без ИП/ипотеки/ТОО — показать меню цифрами."""
    low = text.lower()
    if not any(
        w in low
        for w in (
            "кредит", "несие", "қарыз", "карыз", "заем", "заём",
            "деньги", "ақша", "акша", "срочно",
        )
    ):
        return False
    specific = (
        "ипотек", "ип ", " жк", "жк ", "тоо", "төо", "даму", "рефинанс",
        "жеке", "физлиц", "бизнес", "ипотека", "пәтер", "квартир",
    )
    return not any(s in low for s in specific)


def get_credit_choice_menu(lang: str) -> str:
    if lang == "kk":
        lead = (
            "🤖 *Чат-бот түсінді:* несие керек.\n\n"
            "*Қай өнім? Тек санын жазыңыз:*\n\n"
        )
    else:
        lead = (
            "🤖 *Чат-бот понял:* вам нужен кредит.\n\n"
            "*Какой продукт? Напишите только цифру:*\n\n"
        )
    return lead + get_main_menu_text(lang)


